  List<FlSpot> generateRandomFlSpots(int maxX, int maxY) {
    List<FlSpot> result = [];
    double incrementalx = maxX / 5;
    double incrementaly = maxY / 5;

    int count = 0;
    while (count < 6) {
      if (count == 0) {
        result.add(const FlSpot(0, 3));
      }
      result
          .add(FlSpot(incrementalx * count, Random().nextInt(maxY).toDouble()));
      count++;
    }
    return result;
  }

